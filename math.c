#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    //数学函数
    printf("sqrt(36.0): %f\n", sqrt(36.0));
    printf("exp(2.0): %f\n", exp(2.0));
    printf("fabs(-36.0): %f\n", fabs(-36.0));
    printf("pow(2.0, 3): %f\n", pow(2.0, 3));
    printf("log(100): %f\n", log(100));
    printf("sin(30*3.14/180): %f\n", sin(30*3.14/180));
    printf("log10(100.0): %f\n", log10(100.0));
    printf("cos(30*3.14/180): %f\n", cos(30*3.14/180));

    return 0;
}
